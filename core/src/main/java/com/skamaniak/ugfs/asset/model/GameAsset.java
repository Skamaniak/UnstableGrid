package com.skamaniak.ugfs.asset.model;

public class GameAsset {
    private String id;
    private String name;
    private String description;
    private AssetType assetType;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public AssetType getAssetType() {
        return assetType;
    }
}
